create extension if not exists supabase_vault with schema vault;

create table if not exists public.app_settings (
    key text primary key,
    value jsonb not null default '{}'::jsonb,
    updated_at timestamptz not null default now(),
    updated_by text not null default 'system'
);

create table if not exists public.crm_provider_configs (
    provider text primary key check (provider in ('amocrm', 'bitrix24')),
    settings jsonb not null default '{}'::jsonb,
    secret_id uuid,
    has_secret boolean not null default false,
    connection_status text not null default 'not_checked'
        check (connection_status in ('not_checked', 'ok', 'error')),
    connection_message text not null default '',
    connection_checked_at timestamptz,
    updated_at timestamptz not null default now(),
    updated_by text not null default 'system'
);

create table if not exists public.crm_delivery_log (
    id bigint generated by default as identity primary key,
    created_at timestamptz not null default now(),
    provider text not null,
    event text not null,
    status text not null,
    message text not null default '',
    lead_reference text,
    idempotency_key text
);

create unique index if not exists crm_delivery_log_idempotency_idx
    on public.crm_delivery_log (idempotency_key)
    where idempotency_key is not null;

alter table public.app_settings enable row level security;
alter table public.crm_provider_configs enable row level security;
alter table public.crm_delivery_log enable row level security;

revoke all on public.app_settings from anon, authenticated;
revoke all on public.crm_provider_configs from anon, authenticated;
revoke all on public.crm_delivery_log from anon, authenticated;

